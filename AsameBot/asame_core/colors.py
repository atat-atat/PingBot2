		#format
		bold = '\033[1m'
		underline = '\033[4m'

		#foreground colors
		cblack = '\033[30m'
		cred = '\033[31m'
		cgreen = '\033[32m'
		cyellow = '\033[33m'
		cblue = '\033[34m'
		cmagenta = '\033[35m'
		ccyan = '\033[36m'
		cwhite = '\033[37m'

		#background colors
		bblack = '\033[40m'
		bred = '\033[41m'
		bgreen = '\033[42m'
		byellow = '\033[43m'
		bblue = '\033[44m'
		bmagenta = '\033[45m'
		bcyan = '\033[46m'
		bwhite = '\033[47m'